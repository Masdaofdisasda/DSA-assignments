package main.java.exercise;

public class SwapElement {
    int i;
    int j;

    public SwapElement(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public void print() {
        System.out.println(this.i + " " + this.j);
    }
}
